    private void chooseRangeRadioButtonStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_chooseRangeRadioButtonStateChanged
        if(chooseRangeRadioButton.isSelected() == true) {
            fromTextField.setVisible(true);
            toTextField.setVisible(true);
            fromLabel.setVisible(true);
            toLabel.setVisible(true);
            iAmReadyButton.setEnabled(true);
        } else {
            fromTextField.setVisible(false);
            toTextField.setVisible(false);
            fromLabel.setVisible(false);
            toLabel.setVisible(false);
        }
    }//GEN-LAST:event_chooseRangeRadioButtonStateChanged

