    private void iAmReadyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iAmReadyButtonActionPerformed
        if(evt.getSource() == iAmReadyButton) {
            if(allQuestionsRadioButton.isSelected() == true)
                if(isInputCorrect(fromTextField, toTextField) == false && allQuestionsRadioButton.isSelected() == false) {
                    JOptionPane.showMessageDialog(null, "You must enter the number for the questions you want to be tested on (1 - 5)");
                } else {
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            new OnlineTestingFrame().setVisible(true);
                        }
                    });
                    this.setVisible(false);
                }
        } else {
            if (isInputCorrect(fromTextField, toTextField) == false && allQuestionsRadioButton.isSelected() == false) { 
                JOptionPane.showMessageDialog(null, "You must enter the number for the questions you want to be tested on (1 - 5)");
            } else {
                java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            new OnlineTestingFrame(fromTextField.getText() , toTextField.getText()).setVisible(true);
                        }
                    });
                    this.setVisible(false);
            }
        }
    }//GEN-LAST:event_iAmReadyButtonActionPerformed

